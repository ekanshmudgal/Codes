#include <stdio.h>
void main()
{
    int a, num, mult;

    printf("Enter the number for which the table is to be printed:\n");
    scanf("%d", &num);

    printf("The reverse table will start from\?\n");
    scanf("%d", &mult);

    for (a=mult; a=mult; mult--)
        {
            printf("%d x %d = %d\n", num, a, num*a);
        }
}